// Helper functions
export function getRecommendationsByType(type: string): Recommendation[] {
  return mockRecommendations.filter((rec) => rec.type === type);
}

export function getRecommendationsByEpisode(episodeId: string): Recommendation[] {
  return mockRecommendations.filter((rec) => rec.episodeId === episodeId);
}

export function getEpisodesByPodcast(podcastId: string): Episode[] {
  return mockEpisodes.filter((ep) => ep.podcastId === podcastId);
}

export function getPodcastById(id: string): Podcast | undefined {
  return mockPodcasts.find((p) => p.id === id);
}

export function getEpisodeById(id: string): Episode | undefined {
  return mockEpisodes.find((e) => e.id === id);
}

export function getRecommendationById(id: string): Recommendation | undefined {
  return mockRecommendations.find((r) => r.id === id);
}
